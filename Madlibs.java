public class Madlibs {
    public static void main(String[] args) {
       System.out.println("Madlibs!");
       
       System.out.println("My _________ friend accidently ____ all my ____.");

       String adjective = "beautiful";
       String verb = "ate";
       String noun = "candy";

       System.out.println("My " + adjective + " friend accidently " + verb + " all my " + noun + "." );
    }
}
